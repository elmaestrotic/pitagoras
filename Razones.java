package com.narvasoft.pitagoras;

public class Razones {
// CO CA CO CA HIP HIP
    public static double seno(double cO, double h) {

        return cO / h;

    }

    public static double coSeno(double cA, double h) {

        return cA / h;

    }

    public static double tanGente(double cO, double cA) {

        return cO / cA;

    }

    public static double seCante(double h, double cA) {

        return h / cA;

    }

    public static double cotanGente(double cA, double cO) {

        return cA / cO;

    }

    public static double coSecante(double h, double cO) {

        return h / cO;

    }

}
